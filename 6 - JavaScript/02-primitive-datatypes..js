// Primitive Data Types

// Strings: textual data enclosed with "" or ''
let lastName = 'Doe';
console.log("Last name:", lastName);
console.log("Data Type of Last Name:", typeof lastName);

// Number: Represent numeric values (30, -30, 3.14)
let age = 30;
console.log("Age:", age);
console.log("Data Type of Age:", typeof age);

// Boolean: Either true or false
let isStudent = false;
console.log("Is Student?", isStudent);
console.log("Data Type of isStudent:", typeof isStudent);

// Null: Intentional absence of value
let car = null;
console.log("Car:", car);
console.log("Data Type of Car:", typeof car);
// Updating the content
car = "AE36";
console.log("Car:", car);
console.log("Data Type of Car:", typeof car);

// Undefined: Unintentional absence of value
let city;
console.log("City:", city);
console.log("Data Type of City:", typeof city);